/**
 * Class contains constructor and getter
 * for Category's class field
 */
public class Category {
  String category;

  /**
   * constructor of class Category
   *
   * @param category - category of sport equipment
   */
  public Category(String category) {
    this.category = category;
  }

  /**
   * getter for category
   *
   * @return category
   */
  public String getCategory() {
    return category;
  }
}